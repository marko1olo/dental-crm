export async function ensureClinicalTemplatesSeeded(
	organizationId: string,
): Promise<void> {
	const existing = await db
		.select()
		.from(visitTemplates)
		.where(eq(visitTemplates.organizationId, organizationId));
	const existingTitles = new Set(existing.map((t) => t.title));

	const toInsert = BUILT_IN_TEMPLATES.filter(
		(t) => !existingTitles.has(t.title),
	);
	if (toInsert.length === 0) {
		console.log("[Templates] All built-in templates already seeded.");
		return;
	}

	for (const template of toInsert) {
		await db.insert(visitTemplates).values({
			...template,
			organizationId,
			isBuiltIn: true,
		});
	}
	console.log(
		`[Templates] Seeded ${toInsert.length} built-in clinical templates.`,
	);
}

import { fileURLToPath } from "node:url";

// Standalone runner
async function seed() {
	const [org] = await db.select().from(organizations).limit(1);
	if (!org) throw new Error("No organization found to seed templates into.");
	await ensureClinicalTemplatesSeeded(org.id);
	process.exit(0);
}

const nodePath = process.argv[1];
if (nodePath) {
	try {
		const currentFilePath = fileURLToPath(import.meta.url);
		if (
			nodePath === currentFilePath ||
			nodePath.endsWith("seedTemplates.ts") ||
			nodePath.endsWith("seedTemplates.js")
		) {
			seed().catch((err) => {
				console.error(err);
				process.exit(1);
			});
		}
	} catch (e) {
		// Ignore URL conversion errors if run from custom environment
	}
}
